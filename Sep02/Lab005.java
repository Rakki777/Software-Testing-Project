package Sep02;

public class Lab005 {
    public static void main(String[] args) {
//        character literals
//        single char enclosed - char only allows 1 character
        char c1 = 'a';
//        System.out.println(c1);
//        Escape Sequence
        char c2 ='b';
        System.out.println(c1 + "\n" + c2);
        System.out.println(c1 + "\t" + c2);
        System.out.println(c1 + "\b" + c2);
        System.out.println(c1 + "\r" + c2);
        System.out.println(c1 + "\f" + c2);
        System.out.println(c1 + "\'" + c2);
        System.out.println(c1 + "\"" + c2);
        System.out.println(c1 + "\\" + c2);
    }
}
