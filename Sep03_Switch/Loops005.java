package Sep03_Switch;

public class Loops005 {
    public static void main(String[] args) {
//        Nested For Loop
        for (int i = 0; i < 3; i++) {
            System.out.println(i);
            for (int j = 0; j < 3; j++){
                System.out.println(j);
            }
            
        }
    }
}
